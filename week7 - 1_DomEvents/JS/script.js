//see complete list of DOM events at
//https://developer.mozilla.org/en-US/docs/Web/Events

const CTA = document.querySelector(".cta a");
const ALERT = document.querySelector("#booking-alert");

CTA.classList.remove("hide");
ALERT.classList.add("hide");

function reveal(e){
  //prevent default object behavior
  e.preventDefault();
  CTA.classList.toggle("hide");
  ALERT.classList.toggle("hide");
}

/////////////////////////////////////////////////////////////////
/////////////////////////////////////////////////////////////////
/////////////////////////Event Handler///////////////////////////
/////////////////////////////////////////////////////////////////
/////////////////////////////////////////////////////////////////
CTA.onclick = reveal;
//handing multiple events won't work. last event takes presidence.
CTA.onclick = console.log("button clicked!");


/////////////////////////////////////////////////////////////////
/////////////////////////////////////////////////////////////////
/////////////////////////Event Listenser/////////////////////////
/////////////////////////////////////////////////////////////////
/////////////////////////////////////////////////////////////////
CTA.addEventListener("click", reveal ,false);
CTA.addEventListener("click", function(){console.log("button clicked!")}, false);
